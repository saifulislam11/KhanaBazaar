CREATE TABLE FOOD_ITEM (
	ID VARCHAR2 ( 15 ) UNIQUE,
	RESTUARANT_ID VARCHAR2 ( 15 ),
	NAME VARCHAR2 ( 50 ),
	PRICE NUMBER,
	OFFER NUMBER,
	AVAILIBILITY CHAR ( 1 ),
	DESCRIPTION VARCHAR2 ( 1000 ),
	TYPE VARCHAR2 ( 50 ),
	CONSTRAINT FOOD_ITEM_PK PRIMARY KEY ( ID, RESTUARANT_ID ),
	CONSTRAINT FOOD_ITEM_FK FOREIGN KEY ( RESTUARANT_ID ) REFERENCES RESTUARANT ( ID ) 
);





















